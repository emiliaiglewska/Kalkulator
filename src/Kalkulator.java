public class Kalkulator {
    public int dodawanie (int x, int y){
        int z;
        z = x + y;
        return z;
    }
    public int odejmowanie (int x, int y){
        int z;
        z = x - y;
        return z;
    }
    public  int mnozenie (int x, int y) {
        return x * y;
    }
    public int dzielenie (int x, int y) {
        int z = x / y;
        return z;
    }
}